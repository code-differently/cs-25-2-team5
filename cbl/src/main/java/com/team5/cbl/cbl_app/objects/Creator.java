package com.team5.cbl.cbl_app.objects;

public class Creator {
  private String name;
  private int age;

  // Constructor
  public Creator(String name, int age) {
    this.name = name;
    this.age = age;
  }

  // Getters
  public String getName() {
    return name;
  }

  public int getAge() {
    return age;
  }
}
